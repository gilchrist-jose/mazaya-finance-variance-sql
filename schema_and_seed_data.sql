-- ------------------------------------------------------------
-- TABLE 3: budget
-- The plan. What each department intended to spend/earn
-- per GL account per month. Set at the start of the fiscal year.
-- NOTE: month stored as DATE using first day of each month.
-- This is cleaner than storing an integer — it keeps time
-- operations (date_trunc, extract) available later.
-- ------------------------------------------------------------
CREATE TABLE budget (
    budget_id       SERIAL PRIMARY KEY,
    dept_id         INT     NOT NULL REFERENCES departments(dept_id),
    account_id      INT     NOT NULL REFERENCES gl_accounts(account_id),
    month           DATE    NOT NULL,
    budget_amount   NUMERIC(15, 2) NOT NULL
);


-- ------------------------------------------------------------
-- TABLE 4: actuals
-- The reality. What was actually spent/earned.
-- Identical structure to budget — this is intentional.
-- The JOIN between these two tables is the entire analysis.
-- ------------------------------------------------------------
CREATE TABLE actuals (
    actual_id       SERIAL PRIMARY KEY,
    dept_id         INT     NOT NULL REFERENCES departments(dept_id),
    account_id      INT     NOT NULL REFERENCES gl_accounts(account_id),
    month           DATE    NOT NULL,
    actual_amount   NUMERIC(15, 2) NOT NULL
);


