-- ============================================================
-- MAZAYA RETAIL GROUP — Finance Expense Variance Reporting
-- Script 1 of 4: Schema Design & Seed Data
-- Dialect: PostgreSQL
-- ============================================================
-- WHY THIS STRUCTURE?
-- We separate reference data (departments, gl_accounts) from
-- transactional data (budget, actuals). This is normalisation.
-- It means each department name or account name is stored ONCE.
-- Every other table references it by ID.
-- Interview answer: "I normalised the schema to eliminate
-- redundancy and ensure a single source of truth for reference data."
-- ============================================================


-- ------------------------------------------------------------
-- TABLE 1: departments
-- Stores each business unit once. Referenced by ID everywhere else.
-- ------------------------------------------------------------
CREATE TABLE departments (
    dept_id     SERIAL PRIMARY KEY,
    dept_name   VARCHAR(100) NOT NULL
);

