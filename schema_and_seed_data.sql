-- ------------------------------------------------------------
-- TABLE 2: gl_accounts
-- GL = General Ledger. These are the spending/revenue categories
-- that every finance department uses to classify transactions.
-- account_type distinguishes EXPENSE from REVENUE — this matters
-- later because variance direction is interpreted differently:
--   EXPENSE:  actual > budget = UNFAVORABLE (overspend)
--   REVENUE:  actual > budget = FAVORABLE   (beat target)
-- account_code is stored as VARCHAR, not INT, because account
-- codes are identifiers not quantities — VARCHAR preserves
-- leading zeros and alphanumeric formats used in real ERP systems.
-- ------------------------------------------------------------
CREATE TABLE gl_accounts (
    account_id      SERIAL PRIMARY KEY,
    account_code    VARCHAR(20)  NOT NULL,
    account_name    VARCHAR(100) NOT NULL,
    account_type    VARCHAR(10)  NOT NULL CHECK (account_type IN ('EXPENSE', 'REVENUE'))
);

